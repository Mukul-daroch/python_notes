list1 = ["0", "1", "2"]
list2 = ["red", "big", "tasty"]
# ________________________________________________________________________________________________
# For Loops
# ------------------------------------------------------------------------------------------------
# A for loop is used for iterating over a sequence
# With the for loop we can execute a set of statements, once for each item in a list, tuple, set etc.
# ------------------------------------------------------------------------------------------------
# for loop
# ____________________________
print("____________________________________________________0")
for x in list1:
    print(x, end=" ")
print()
# ------------------------------------------------------------------------------------------------
# Looping Through a String
# ____________________________
print("____________________________________________________1")
for x in "banana":
    print(x)
# ------------------------------------------------------------------------------------------------
# break
# ____________________________
print("____________________________________________________2")
for x in list1:
    print(x)
    if x == "1":
        break
print("________________________")
for x in list1:
    if x == "1":
        break
    print(x)
# ------------------------------------------------------------------------------------------------
# continue
# ____________________________
print("____________________________________________________3")
for x in list1:
    if x == "1":
        continue
    print(x)
# ------------------------------------------------------------------------------------------------
# range()
# ____________________________
print("____________________________________________________4")
for x in range(3):
    print(x)
print("________________________")
for x in range(1, 20, 3):
    print(x)
# ------------------------------------------------------------------------------------------------
# Else in For Loop
# ____________________________
# The else keyword in a for loop specifies a block of code to be executed when the loop is finished:
print("____________________________________________________5")
for x in range(6):
    print(x)
else:
    print("Finally finished!")

print("________________________")
for x in range(6):
    if x == 3:
        break
    print(x)
else:
    print("Finally finished!")
# ------------------------------------------------------------------------------------------------
# Nested Loops
# ____________________________
# A nested loop is a loop inside a loop
## syntax ##
# for x in y:
#    for t in x:
#        print(t)
print("____________________________________________________6")
print(list2)
print(list1)
for x in list2:
    for y in list1:
        print(x, y)
# ------------------------------------------------------------------------------------------------
# The pass Statement
# ____________________________
# For loops cannot be empty, but if you for some reason have a for loop with no content, put in the pass statement to avoid getting an error.
print("____________________________________________________7")
for x in [0, 1, 2]:
    pass
print("_____________________________________________________")
