# Dictionaries
# ------------------------------------------------------------------------------------------------
# Dictionaries are used to store data values in key:value pairs.
# A dictionary is a collection which is ordered*, changeable and do not allow duplicates.
# ------------------------------------------------------------------------------------------------
# ------------------------------------------------------------------------------------------------
# Create and print a dictionary
# ____________________________
print("_______________________________________________0")
thisdict = {"A": "sting", "B": 2, "C": True}
thisdict =dict(A= "sting", B= 2, C= True)
print(thisdict)
# ------------------------------------------------------------------------------------------------
# "brand" value of the dictionary
# ____________________________
print("_______________________________________________1")
thisdict = {"A": "sting", 1: "B", "C": True}
print(thisdict[1])

# ------------------------------------------------------------------------------------------------
# Duplicates Not Allowed
# ____________________________
# Dictionaries cannot have two items with the same key
print("_______________________________________________2")
thisdict = {"A": "sting", "B": 1, "C": True, "C": False}
print(thisdict)

# ------------------------------------------------------------------------------------------------
# Dictionary Length
# ____________________________
# Print the number of items in the dictionary
print("_______________________________________________3")
print(len(thisdict))
# ________________________________________________________________________________________________
# Data Types
# ____________________________
# The values in dictionary items can be of any data type
print("_______________________________________________4")
thisdict = {"A": "sting", "B": 1, "C": True, "D": ["a", "b", "c"]}
print(thisdict["D"][0])
